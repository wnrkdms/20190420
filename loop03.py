#range 함수 연습하기

print("1부터 10까지")
for i in range (1,10,1):
    print(i,end=" ")
print()

print("0부터 9까지")
for i in range(10):
    print(i,end=" ")
print()

print("0부터 10까지")
for i in range(11):
    print(i,end=" ")
print()

print("1부터 10까지")
for i in range(1,11):
    print(i,end=" ")
print()

print("1부터 10까지의 수 중에 홀수")
for i in range(1,11,2):
    print(i,end=" ")
print()

print("1부터 10까지의  수 중에 짝수")
for i in range(2,11,2):
    print(i,end=" ")
print()
